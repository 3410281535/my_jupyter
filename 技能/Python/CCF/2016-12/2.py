    s += 3500
    if a <= 1500 * 0.97:
        s += a  / 0.97
    elif 1500 * 0.97 < a <= 1500 * 0.97 + 3000 * 0.9:
        s += 1500  + (a - 1500 * 0.97) / 0.9
    elif 1500 * 0.97 + 3000 * 0.9 < a <= 1500 * 0.97 + 3000 * 0.9 + 4500 * 0.8:
        s += 1500 + 3000  + (a - 1500 * 0.97 - 3000 * 0.9) / 0.8
    elif 1500 * 0.97 + 3000 * 0.9 + 4500 * 0.8 < a <= 1500 * 0.97 + 3000 * 0.9 + 4500 * 0.8 + 26000 * 0.75:
        s += 1500 + 3000  + 4500  + (a - 1500 * 0.97 - 3000 * 0.9 - 4500 * 0.8) / 0.75
